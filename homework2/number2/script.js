let num = prompt('Введите цифру от 0 до 9')

if (+num === 0) {
    alert('Ваш символ: )')
}
else if (+num === 1) {
    alert('Ваш символ: !')
}
else if (+num === 2) {
    alert('Ваш символ: @')
}
else if (+num === 3) {
    alert('Ваш символ: #')
}
else if (+num === 4) {
    alert('Ваш символ: $')
}
else if (+num === 5) {
    alert('Ваш символ: %')
}
else if (+num === 6) {
    alert('Ваш символ: ^')
}
else if (+num === 7) {
    alert('Ваш символ: &')
}
else if (+num === 8) {
    alert('Ваш символ: *')
}
else if (+num === 9) {
    alert('Ваш символ: (')
}
else if (+num > 9) {
    alert('error')
}
